FROM
(
  SELECT PHOTO
  FROM HAKMAIN.SEA013
  WHERE COLREG_NO = :admin_id

  UNION ALL
  
  SELECT  PHOTO
  FROM HAKMAIN.SEB013
  WHERE COLREG_NO = :admin_id
  
  UNION ALL
  
  SELECT PICTURE
  FROM ADMAIN.AIA010
  WHERE EMP_NO = :admin_id
  
  UNION ALL
  
  SELECT PICTURE
  FROM ADMAIN.ATA020
  WHERE EMP_NO = :admin_id
  
  UNION ALL
  
  SELECT PICTURE
  FROM ADMAIN.SANHAK_LIB
  WHERE EMP_NO = :admin_id
)
WHERE ROWNUM = 1